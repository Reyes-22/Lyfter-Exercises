'''Create an algorithm that uses print() to display your full name, your age, your favorite color, and 
your favorite food. Each piece of information should be displayed on a separate line.
'''
my_name = "My name is Bryan Reyes"
my_age = "I am 29 years old"
favorite_color = "My favorite color is black"
favorite_food = "My favorite food is hamburgers"

print(my_name)
print(my_age)
print(favorite_color)
print(favorite_food, "\n")

'''Create an algorithm that shows how old you will be in 10 years.
The algorithm should use `print()` and an addition operation.'''

my_age = 29
age_in_10_years = my_age + 10

print("In 10 years, I will be", age_in_10_years, "years old.", "\n")


